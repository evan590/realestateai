// Helper function to filter properties
export function filterProperties(properties: Property[], filters: import('@/types').PropertyFilters): Property[] {
  return properties.filter(property => {
    if (filters.minPrice && property.price < filters.minPrice) return false;
    if (filters.maxPrice && property.price > filters.maxPrice) return false;
    if (filters.minBeds && property.bedrooms < filters.minBeds) return false;
    if (filters.maxBeds && property.bedrooms > filters.maxBeds) return false;
    if (filters.minBaths && property.bathrooms < filters.minBaths) return false;
    if (filters.maxBaths && property.bathrooms > filters.maxBaths) return false;
    if (filters.minSqft && property.sqft < filters.minSqft) return false;
    if (filters.maxSqft && property.sqft > filters.maxSqft) return false;
    if (filters.propertyType?.length && !filters.propertyType.includes(property.property_type)) return false;
    if (filters.city && !property.city.toLowerCase().includes(filters.city.toLowerCase())) return false;
    if (filters.state && property.state.toLowerCase() !== filters.state.toLowerCase()) return false;
    return true;
  });
}

// Get a single property by ID
export function getPropertyById(id: string): Property | undefined {
  return mockProperties.find(p => p.id === id);
}

// Format price for display
export function formatPrice(price: number): string {
  return new Intl.NumberFormat('en-US', {
    style: 'currency',
    currency: 'USD',
    maximumFractionDigits: 0,
  }).format(price);
}

// Format number with commas
export function formatNumber(num: number): string {
  return new Intl.NumberFormat('en-US').format(num);
}
